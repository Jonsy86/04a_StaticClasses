package com.cc.java;

public class RechnerInst {

	int summe(int a, int b){
		return a+b;
	}
	
	int differenz(int a, int b){
		return a-b;
	}
	
	int produkt(int a, int b){
		return a*b;
	}
	
	double quotient(double a, double b){ // double --> Rechnen mit Kommazahlen
		return a/b;
	}
	

} // EoC
